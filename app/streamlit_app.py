# ================= PDF FUNCTION =================
class PDF(FPDF):
    def header(self):
        self.set_font("Arial", "B", 16)
        self.set_text_color(0, 102, 204)
        self.cell(0, 10, "AI Resume Analysis Report", ln=True, align="C")
        self.ln(5)

    def section_title(self, title):
        self.set_font("Arial", "B", 12)
        self.set_text_color(50, 50, 50)
        self.cell(0, 10, title, ln=True)
        self.ln(2)

    def section_body(self, items):
        self.set_font("Arial", "", 11)
        for item in items:
            self.multi_cell(0, 8, f"- {item}")
        self.ln(3)


def generate_pdf_buffer(skills, score, missing, suggestions):
    pdf = PDF()
    pdf.add_page()

    pdf.set_font("Arial", "B", 14)
    pdf.set_text_color(0, 153, 76)
    pdf.cell(0, 10, f"Overall Score: {score}%", ln=True)
    pdf.ln(5)

    pdf.section_title("Extracted Skills")
    pdf.section_body(skills if skills else ["No skills detected"])

    pdf.section_title("Missing Skills")
    pdf.section_body(missing if missing else ["No major missing skills"])

    pdf.section_title("Suggestions")
    pdf.section_body(suggestions if suggestions else ["No suggestions"])

    pdf.ln(10)
    pdf.set_font("Arial", "I", 9)
    pdf.set_text_color(100, 100, 100)
    pdf.multi_cell(0, 8, "Generated by AI Resume Analyzer")

    pdf_output = pdf.output(dest='S').encode('latin-1')
    return BytesIO(pdf_output)


