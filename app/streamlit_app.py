st.markdown("<p style='text-align:center;'>Smart Resume Evaluation using AI</p>", unsafe_allow_html=True)

uploaded_file = st.file_uploader("Upload Resume (PDF)", type=["pdf"])
job_desc = st.text_area("Paste Job Description")


# ================= MAIN =================
if uploaded_file:
    text = extract_text(uploaded_file)

    # 🔥 DEBUG SCORES
    r_score = rule_score(text)
    m_score = ml_score(text)
    final_score = (0.6 * (r_score / 100)) + (0.4 * m_score)

    st.write("Rule Score:", r_score)
    st.write("ML Score:", m_score)
    st.write("Final Score:", final_score)

    # 🔥 CHECK
    if final_score <= 0.7:
        st.error("❌ Uploaded file does not appear to be a valid resume.")
        st.stop()

    if not text or len(text.strip()) < 50:
        st.error("❌ Resume content is too small or unreadable.")
        st.stop()

    # 🔥 Resume validation
    if not is_resume(text):
        st.error("❌ Uploaded file does not appear to be a valid resume.")
        st.stop()

    skills_list = load_skills()
    skills = extract_skills(text, skills_list)

    col1, col2 = st.columns(2)

    # 🔹 SKILLS
    with col1:
        st.subheader("🧠 Extracted Skills")
        st.markdown('<div class="card">', unsafe_allow_html=True)

        if skills:
            for skill in skills:
                st.markdown(f'<span class="skill-box">{skill}</span>', unsafe_allow_html=True)
        else:
            st.warning("No skills detected")

        st.markdown('</div>', unsafe_allow_html=True)

    # 🔹 JOB MATCH
    if job_desc and len(job_desc.strip()) > 20:

        score = match_score(text, job_desc)
        missing = missing_skills(skills, job_desc, skills_list)
        ats = ats_score(text, skills, job_desc)
        suggestions = generate_suggestions(missing, ats["Total Score"])

        # 🔹 SCORE
        with col2:
            st.markdown('<div class="card">', unsafe_allow_html=True)

            colA, colB, colC = st.columns(3)
            colA.metric("Total Score", f"{ats['Total Score']}%")
            colB.metric("Skill Score", ats["Skill Score"])
            colC.metric("Keyword Score", ats["Keyword Score"])

            st.progress(ats["Total Score"] / 100)

            st.markdown('</div>', unsafe_allow_html=True)

        # 🔹 MISSING SKILLS
        st.subheader("⚠️ Missing Skills")
        st.markdown('<div class="card">', unsafe_allow_html=True)

        if missing:
            for m in missing:
                st.markdown(f"❌ **{m}**")
        else:
            st.success("No major missing skills 🎉")

        st.markdown('</div>', unsafe_allow_html=True)

        # 🔹 SUGGESTIONS
        st.subheader("💡 Suggestions")
        st.markdown('<div class="card">', unsafe_allow_html=True)

        for s in suggestions:
            st.markdown(f"✅ {s}")

        st.markdown('</div>', unsafe_allow_html=True)

        # 🔹 PDF DOWNLOAD
        st.markdown("<br>", unsafe_allow_html=True)

        col_center = st.columns([1,2,1])[1]

        with col_center:
            if st.button("📄 Generate Report"):
                pdf_buffer = generate_pdf_buffer(
                    skills,
                    ats["Total Score"],
                    missing,
                    suggestions
                )

                st.download_button(
                    "⬇️ Download Report",
                    pdf_buffer,
                    file_name="resume_report.pdf",
                    mime="application/pdf"
                )

    else:
        st.info("👉 Please paste a valid job description to see analysis.")